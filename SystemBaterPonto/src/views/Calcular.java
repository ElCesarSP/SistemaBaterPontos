    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String horasDeTrabalho = txthoratab.getText();
        String diasTrabalho = txtdias.getText();

        // Converter os valores de entrada para números inteiros
        int horas = Integer.parseInt(horasDeTrabalho);
        int dias = Integer.parseInt(diasTrabalho);

        // Calcular a quantidade de horas de trabalho por dia
        int horasPorDia = horas * dias;
        
        txthoratab.setText("");
        txtdias.setText("");

        // Exibir o resultado no campo de texto "txtresultado"
        txtresultado2.setText(Integer.toString(horasPorDia));
    }//GEN-LAST:event_jButton3ActionPerformed

