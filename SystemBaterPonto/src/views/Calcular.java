    private void CalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalcularActionPerformed
        // TODO add your handling code here:
        String hora = txthora.getText();
        String valor = txtvalor.getText();

// Verifica se as strings não estão vazias
        if (!hora.isEmpty() && !valor.isEmpty()) {
            try {
                double horaTrabalhada = Double.parseDouble(hora);
                double valorTrabalhado = Double.parseDouble(valor);

                double resultado = horaTrabalhada * valorTrabalhado;

                // Define o texto do componente txtresultado com o resultado da multiplicação
                txtresultado.setText("Resultado: " + resultado);
            } catch (NumberFormatException e) {
                // Tratar caso as strings não possam ser convertidas para números válidos
                // Aqui você pode exibir uma mensagem de erro ou tomar alguma outra ação adequada
                JOptionPane.showMessageDialog(null, "Os valores inseridos não são números válidos, tenta usar ( . )!");
            }
        } else {
            // Tratar caso uma ou ambas as strings estejam vazias
            // Aqui você pode exibir uma mensagem de erro ou tomar alguma outra ação adequada
            JOptionPane.showMessageDialog(null, "Preencha todos os campos necessários.");
        }
        txthora.setText("");
        txtvalor.setText("");
    }//GEN-LAST:event_CalcularActionPerformed

